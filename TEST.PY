def python_heading_imput(title):
    str_title = str(title)
    print("#" * (len(str_title) + 4))
    print("# " + str(title) + " #")
    print("#" * (len(str_title) + 4))

python_heading_imput("Question 2: Legendre's Conjecture - Part 2!")
